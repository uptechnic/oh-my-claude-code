/**
 * Utility to read/write .env files for API configuration persistence.
 * Ensures offline-mode settings survive across restarts.
 */

import { existsSync, readFileSync, writeFileSync } from 'fs'
import { join } from 'path'

const ENV_FILENAME = '.env'

/** Get the path to the .env file in the working directory. */
function getEnvFilePath(): string {
  return join(process.cwd(), ENV_FILENAME)
}

/** Parse a .env-style string into key-value pairs. */
export function parseEnvFile(content: string): Record<string, string> {
  const result: Record<string, string> = {}
  for (const line of content.split('\n')) {
    const trimmed = line.trim()
    if (!trimmed || trimmed.startsWith('#')) continue
    const eqIndex = trimmed.indexOf('=')
    if (eqIndex === -1) continue
    const key = trimmed.slice(0, eqIndex).trim()
    let value = trimmed.slice(eqIndex + 1).trim()
    // Strip surrounding quotes
    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1)
    }
    result[key] = value
  }
  return result
}

/** Serialize key-value pairs to .env format. */
function serializeEnvFile(vars: Record<string, string>): string {
  const lines: string[] = ['# Generated by Claude Code /login', '']
  for (const [key, value] of Object.entries(vars)) {
    if (value.includes('"') || value.includes("'") || value.includes(' ')) {
      lines.push(`${key}="${value}"`)
    } else {
      lines.push(`${key}=${value}`)
    }
  }
  lines.push('')
  return lines.join('\n')
}

/** Read existing .env file, return parsed key-value pairs. */
export function readEnvFile(): Record<string, string> {
  const path = getEnvFilePath()
  if (!existsSync(path)) return {}
  return parseEnvFile(readFileSync(path, 'utf-8'))
}

/**
 * Write (or merge) key-value pairs into the .env file.
 * Existing keys in the file are updated; new keys are appended.
 */
export function writeEnvFile(vars: Record<string, string>): void {
  const existing = readEnvFile()
  const merged = { ...existing, ...vars }
  writeFileSync(getEnvFilePath(), serializeEnvFile(merged), 'utf-8')
}

/**
 * Set environment variables both on process.env (for the current session)
 * and persist them to the .env file (for future sessions).
 */
export function configureEnvVars(vars: Record<string, string>): void {
  // Apply to current process
  for (const [key, value] of Object.entries(vars)) {
    process.env[key] = value
  }
  // Persist to .env
  writeEnvFile(vars)
}

